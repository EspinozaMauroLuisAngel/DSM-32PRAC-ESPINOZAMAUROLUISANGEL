
// Ejemplo de funciones declarativas

function suma (){
    var num1=10; 
    var num2=90;
    return num1+num2;
}
suma();

function resta (){
    var num1=100; 
    var num2=50;
    return num1-num2;
}
resta();

function multiplicacion(){
    var num1=10; 
    var num2=10;
    return num1*num2;
}
multiplicacion();

function division (){
    var num1=8; 
    var num2=4;
    return num1/num2;
}
division();

function areaCuadrado (){
    var lado=10; 
    return lado*lado;
}
areaCuadrado();