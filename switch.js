// Ejemplo 1
var años = 10;
var resultado;
switch (años){
    case 10:
        resultado = "Si Tienes 10 años";
        break
    case 20:
        resultado = "Si Tienes 20 años";
        break
    case 30:
        resultado = "Si Tienes 30 años";
        break
        default:
            resultado = "No existe";
            console.log ("resultado")
}
